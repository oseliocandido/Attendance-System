class Attendance:
    def __init__(self, user_id, date, type, time):
        self.user_id = user_id
        self.date = date
        self.type = type
        self.time = time

class AttendanceName:
    def __init__(self, name, date, type, time):
        self.name = name
        self.date = date
        self.type = type
        self.time = time
